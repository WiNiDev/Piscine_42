int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int	i;

	i = 0;
	if (n < 1)
		return (0);
	while (s1[i] && s2[i] && (s1[i] == s2[i]) && n > 0)
	{
		n--;
		if (n > 0)
			i++;
	}
	return (s1[i] - s2[i]);
}
